package com.streamreduce.util;
public class InvalidOutboundConfigurationException extends RuntimeException {

    @SuppressWarnings("unused")
    public InvalidOutboundConfigurationException() {
    }

    @SuppressWarnings("unused")
    public InvalidOutboundConfigurationException(String s) {
        super(s);
    }

    @SuppressWarnings("unused")
    public InvalidOutboundConfigurationException(String s, Throwable throwable) {
        super(s, throwable);
    }

    @SuppressWarnings("unused")
    public InvalidOutboundConfigurationException(Throwable throwable) {
        super(throwable);
    }
}
